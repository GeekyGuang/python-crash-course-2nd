def city_country(city, country):
  city_country = f"{city}, {country}"
  return city_country

city_country1 = city_country('BeiJing', 'China')
city_country2 = city_country('Paris', 'France')
city_country3 = city_country('Berlin', 'Germany')

print(city_country1)
print(city_country2)
print(city_country3)