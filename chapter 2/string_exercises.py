# Name Cases
name = 'nico robin'
print(name.title())
print(name.upper())
print(name.lower())
# Famous Quote
print('Albert Einstein once said, "A person who never' +
 ' made a mistake never tried anything new."')

famous_person = "Albert Einstein"
print(f'{famous_person} once said, "A person who never made a mistake never tried anything new."')

# Stripping Names
famous_person = "\t\n\tAlbert Einstein\t\n\t\t"
print(famous_person)
print(famous_person.lstrip())
print(famous_person.rstrip())
print(famous_person.strip())


